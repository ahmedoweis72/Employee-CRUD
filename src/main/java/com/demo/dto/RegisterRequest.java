package com.demo.dto;

import jakarta.validation.constraints.*;

/**
 * DTO for user registration.
 * Used to receive registration data from clients (e.g. /api/auth/register).
 */
public class RegisterRequest {

    @NotBlank(message = "Username is required")
    @Size(min = 3, max = 30, message = "Username must be between 3 and 30 characters")
    private String username;

    @NotBlank(message = "First name is required")
    @Size(max = 50, message = "First name cannot exceed 50 characters")
    private String firstName;

    @NotBlank(message = "Last name is required")
    @Size(max = 50, message = "Last name cannot exceed 50 characters")
    private String lastName;

    @NotBlank(message = "Password is required")
    @Size(min = 6, max = 100, message = "Password must be at least 6 characters long")
    private String password;

    @NotNull(message = "Age is required")
    @Min(value = 18, message = "Employee must be at least 18 years old")
    @Max(value = 65, message = "Employee must be 65 years old or younger")
    private Integer age;

    @NotBlank(message = "Role is required")
    @Pattern(regexp = "USER|MANAGER|ADMIN", message = "Role must be USER, MANAGER, or ADMIN")
    private String role;

    // ðŸ§© Getters and Setters
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
